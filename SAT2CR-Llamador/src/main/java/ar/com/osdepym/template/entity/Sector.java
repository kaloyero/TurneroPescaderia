package ar.com.osdepym.template.entity;

public class Sector {
	
	private String codSector;
	private String nomSector;
	private String idSector;
	private String habilitado;
	private String nrosEspera;
	
	
	
	public String getCodSector() {
		return codSector;
	}
	public void setCodSector(String codSector) {
		this.codSector = codSector;
	}
	public String getNomSector() {
		return nomSector;
	}
	public void setNomSector(String nomSector) {
		this.nomSector = nomSector;
	}
	public String getSector() {
		return idSector;
	}
	public void setSector(String sector) {
		this.idSector = sector;
	}
	public String getHabilitado() {
		return habilitado;
	}
	public void setHabilitado(String habilitado) {
		this.habilitado = habilitado;
	}
	public String getNrosEspera() {
		return nrosEspera;
	}
	public void setNrosEspera(String nrosEspera) {
		this.nrosEspera = nrosEspera;
	}
	public String getIdSector() {
		return idSector;
	}
	public void setIdSector(String idSector) {
		this.idSector = idSector;
	}
	
	

}
